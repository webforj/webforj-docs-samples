package com.webforj.samples.views.table;

public class MusicRecord {
  private String number;
  private String title;
  private String artist;
  private String label;
  private String playingTime;
  private String recordingType;
  private String musicType;
  private String binLocation;
  private int numberOfTracks;
  private int onHand;
  private double cost;
  private double retail;

  /**
   * @return the number
   */
  public String getNumber() {
    return number;
  }

  /**
   * @param number the number to set
   */
  public void setNumber(String number) {
    this.number = number;
  }

  /**
   * @return the title
   */
  public String getTitle() {
    return title;
  }

  /**
   * @param title the title to set
   */
  public void setTitle(String title) {
    this.title = title;
  }

  /**
   * @return the artist
   */
  public String getArtist() {
    return artist;
  }

  /**
   * @param artist the artist to set
   */
  public void setArtist(String artist) {
    this.artist = artist;
  }

  /**
   * @return the label
   */
  public String getLabel() {
    return label;
  }

  /**
   * @param label the label to set
   */
  public void setLabel(String label) {
    this.label = label;
  }

  /**
   * @return the playingTime
   */
  public String getPlayingTime() {
    return playingTime;
  }

  /**
   * @param playingTime the playingTime to set
   */
  public void setPlayingTime(String playingTime) {
    this.playingTime = playingTime;
  }

  /**
   * @return the recordingType
   */
  public String getRecordingType() {
    return recordingType;
  }

  /**
   * @param recordingType the recordingType to set
   */
  public void setRecordingType(String recordingType) {
    this.recordingType = recordingType;
  }

  /**
   * @return the musicType
   */
  public String getMusicType() {
    return musicType;
  }

  /**
   * @param musicType the musicType to set
   */
  public void setMusicType(String musicType) {
    this.musicType = musicType;
  }

  /**
   * @return the binLocation
   */
  public String getBinLocation() {
    return binLocation;
  }

  /**
   * @param binLocation the binLocation to set
   */
  public void setBinLocation(String binLocation) {
    this.binLocation = binLocation;
  }

  /**
   * @return the numberOfTracks
   */
  public int getNumberOfTracks() {
    return numberOfTracks;
  }

  /**
   * @param numberOfTracks the numberOfTracks to set
   */
  public void setNumberOfTracks(int numberOfTracks) {
    this.numberOfTracks = numberOfTracks;
  }

  /**
   * @return the onhand
   */
  public int getOnHand() {
    return onHand;
  }

  /**
   * @param onhand the onhand to set
   */
  public void setOnHand(int onhand) {
    this.onHand = onhand;
  }

  /**
   * @return the cost
   */
  public double getCost() {
    return cost;
  }

  /**
   * @param cost the cost to set
   */
  public void setCost(double cost) {
    this.cost = cost;
  }

  /**
   * @return the retail
   */
  public double getRetail() {
    return retail;
  }

  /**
   * @param retail the retail to set
   */
  public void setRetail(double retail) {
    this.retail = retail;
  }
}
